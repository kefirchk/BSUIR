#include "Header.h"                             //подключение библиотеки

int offer() //функция предлагает продолжить выполнение программы  
{
	char symbol;                                 //объявление промежуточной переменной
	printf("\nХотите продолжить?(y\\n)");        //приглашение на продолжение выполнения программы
	for (;;) {
		rewind(stdin);                           //чистка буфера
		symbol = _getch();                       //ввод y или n(выбор для продолжения или нет)
		if (symbol == 'y' || symbol == 'n')      //условие выхода из цикла
		{
			system("cls");                       //чистка консоли
			break;                               //выход из цикла
		}
		else {                                   //иначе
			printf("\nОшибка!\n");               //текст об ошибке
			printf("Хотите продолжить?(y\\n)");
		}
	}
	if (symbol == 'n') return 0;                 //выход из цикла, если выполняется условие
	else return 1;
}