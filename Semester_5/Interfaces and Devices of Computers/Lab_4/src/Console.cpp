#include "Console.h"

// ====================================================================
// Конструктор по умолчанию
// --------------------------------------------------------------------
Console::Console()
{
	hWnd = GetConsoleWindow();
}

// ====================================================================
// Спрятать консоль
// --------------------------------------------------------------------
void Console::hide()
{
	ShowWindow(hWnd, SW_HIDE);
}

// ====================================================================
// Показать консоль
// --------------------------------------------------------------------
void Console::show()
{
	ShowWindow(hWnd, SW_SHOW);
}