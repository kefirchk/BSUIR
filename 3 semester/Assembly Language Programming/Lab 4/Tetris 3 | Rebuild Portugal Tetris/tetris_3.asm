CHECK_COLLISION proc  ; Receives position in DX and checks if tetramino will collide(STOLKNULIS'); Return result in AX
    PushAXBXCXDX
    mov CX,DX                 ; Store received position
    call GET_FIGURE_POSITION   ; Pick Piece and current position
    
    push DX
    mov BX, 20                ; Displacement (offset, "smeschenie")
    mul BX                    ; Take hold through the number * displacement
    mov SI, offset FIGURE_T
    xor AH,AH
    add SI, AX                ; Save figure's address
    pop DX
    check_displacement:
        cmp CH, DH            ; Check if displacement went down
        jne checkBellow 
        cmp CL, DL
        jns checkRight        ; Check if shift("sdvig") was to the left (jns - there is a sign)
        jmp checkLeft         ; Check if shift was to the right
    
    checkBellow:   
        ;mov BX, DX
        ;mov DX, CX
        inc DH
        mov CX, 16            ; Set counter

        innerCheckBellow:
            
            mov AX, [SI]      ; Get the part position value 
            xor AH, AH

            cmp AX, BLOCK     ; Check if part position is a square
            jne innerBellowNoValue  ; If not continue loop

            push AX
            add DH, 2
            sub DL, 3
            call GOTO_XY      ; Position cursor on need(desired) element
            mov AH, 08h       ; Instruction for reading the screen character 
            int 10H 
            mov BX,AX         ; Store read pixel value in BX
            sub DH, 2
            add DL, 3
            call CHAR_DISPLAY
            pop AX
            cmp BH, 0h
            je innerBellowNoValue
            xor BH, BH
            cmp BX, BLOCK
            jne achou 
            innerBellowNoValue:
            inc SI
            inc DL
            cmp [SI], '$'
            jne skipIncLine
            inc DH
            sub DL, 4
            inc SI
            skipIncLine:
        loop innerCheckBellow
        jmp end_check_collision
    checkLeft:
       call GET_FIGURE_POSITION
       push ax
       mov ah, 0ch
       mov al, dl
       inc al
       cmp ah, AL
       je  LeftWall
       jne LeftGood
       LeftWall:
       inc DL
       inc DL
       call UPDATE_CURR_POSITION
       LeftGood:
       pop ax
       jmp end_check_collision 
    
    ;you must add code here
          
    checkRight:    
    
    ;you must add code here
    
    achou:
        mov SI, offset GAME_SCORE
        add [SI], 100
        call PRINT_SCORE
      

    end_check_collision:
    popAXBXCXDX
ret
endp
