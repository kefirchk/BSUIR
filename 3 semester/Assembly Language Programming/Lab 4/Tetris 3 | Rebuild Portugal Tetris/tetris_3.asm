DOWN_LINE proc
    ; Delete Figure
    mov AH,1
    call PRINT_FIGURE

    ; Line down
    inc DH

    ;call CHECK_COLLISION

    ; Increment Score
    mov SI, offset GAME_SCORE
    inc [SI]
    call PRINT_SCORE 
    
    ; Print Figure
    mov AH,0
    call PRINT_FIGURE
    call UPDATE_CURR_POSITION
    call UPDATE_TIME  
ret
endp
