PRINT_GENERIC_FIGURE proc ; SI = offset piece ; HD = part line; DA = part column; BL = part color
  PushAXBXCXDX
  mov CX,4
  .print:
    call PUTS_STRING
    inc DH
    inc SI
    loop .print
  PopAXBXCXDX 
  ret
endp
