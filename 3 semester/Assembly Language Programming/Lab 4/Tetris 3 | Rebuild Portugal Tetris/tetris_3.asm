UPDATE_TIME proc ;Save the current time in the last Time variable
    PushAXBXCXDX
    mov  AX, 0h                     ; Get System-Timer Timer
    int  1Ah                        ; Interruption for work with time

    mov  word ptr LAST_READING_TIME, DX      ; DX = Low-order part of clock count
    mov  word ptr LAST_READING_TIME + 2, CX  ; CX = High-order part of clock count
    PopAXBXCXDX
    ret
endp
;----------------------------------------------------------------------------------------------
CHECK_LAST_TIME proc ;Save in TIME_DIFFERENCE elapsed between the current time and the time saved in LAST_READING_TIME
    PushAXBXCXDX
    mov  AX, 0h
    int  1Ah
    mov  AX, word ptr LAST_READING_TIME
    sub  DX, AX
    mov  word ptr TIME_DIFFERENCE, DX
    mov  AX, word ptr LAST_READING_TIME + 2
    sbb  CX, AX
    mov  word ptr TIME_DIFFERENCE + 2, CX
    PopAXBXCXDX
    mov  AX, word ptr TIME_DIFFERENCE
    ret
endp
