GET_FIGURE_POSITION proc
    mov SI, offset CURR_W
    mov DH, [SI]
    mov SI, offset CURR_H
    mov DL, [SI]
    mov SI, offset CURR_FIGURE_NUM
    mov AL, [SI]
ret
endp
;----------------------------------------------------------------------------------------------
UPDATE_CURR_POSITION proc
    mov SI, offset CURR_W
    mov [SI], DH
    mov SI, offset CURR_H
    mov [SI], DL
ret
endp
;----------------------------------------------------------------------------------------------
ADD_NEXT_FIGURE proc  ; Add Next Figure on screen
    PushAXBXCXDX
    mov SI, offset NEXT_FIGURE_NUM
    mov AL, [SI]                    ; Get current value and put it in AL
    mov SI, offset CURR_FIGURE_NUM
    mov [SI], AL                    ; Put new part
    mov DL, 10h                     ; Set new position x,y
    mov DH, 2h
    call UPDATE_CURR_POSITION       ; Update current position
    call PRINT_FIGURE               ; Print piece
    PopAXBXCXDX
    call GENERATE_NEXT_FIGURE       ; Generate next play piece
ret
endp
