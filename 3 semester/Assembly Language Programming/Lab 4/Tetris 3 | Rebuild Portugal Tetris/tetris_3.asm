CHECK_INPUT proc
    PushAXBXCX
    call UPDATE_TIME
    check_again:
        push AX                 ; Store AX value to be able to read from keyboard
        mov AH, 01h             ; Read the buffer
        mov AL, 00h
        int 16h                 ; Reading interruption
        jz exit_check_input     ; If we don't have anything to read, skip to the end of the loop.
        mov ah, 0               ; Get value from keyboard
        int 16h 
        
        ; Delete Current Figure
        push AX                 ; Stacks read value
        mov SI, offset CURR_FIGURE_NUM ; Get current part number
        mov AX, [SI]
        mov AH, 1               ; Option to write the part in black
        call PRINT_FIGURE       ; Print the piece (as it is black, it will erase from the screen)
        pop AX                  ; Unstack read value
    
        ; Moves the Figure according to the value read on the keyboard
        cmp AX, KeyRight        ; Check if it was right arrow
        je moveRight            
        cmp AX, KeyLeft         ; Check if it was left arrow
        je moveLeft 
        jne exit_check_input ; Did not read valid value, so skips other read checks
        
        moveLeft:
        dec DL                  ; Decrement Column
        call CHECK_COLLISION  
        
        jmp print_figure_check
        
        moveRight:
        inc DL
        call CHECK_COLLISION  


        print_figure_check:
        pop AX
        mov AH,0          ; Option parameter to print colored part
        call PRINT_FIGURE ; Print Piece
        call UPDATE_CURR_POSITION
        jmp exit_check_input_2

        exit_check_input:    
        pop AX            ; Returns original AX value before keyboard reading
        
        exit_check_input_2:
        push AX
        call CHECK_LAST_TIME
        mov CX, AX
        pop AX
        cmp  CX, 0Fh
        jna  check_again

    PopAXBXCX
ret
endp
