   mov BL, 02h           ; Color
   mov DH, 05h           ; Line, y
   mov DL, 0Bh           ; Column, x
   call PUTS_STRING
  
   ; Displays Tetrafigures
   mov DH, 09h ; starting line
   mov DL, 05h ; starting column
