GAME_SCREEN proc
    PushAXBXCXDX   
    call CLEAR_SCREEN
    
    ; Displays Score
    mov  word ptr GAME_SCORE, 0
    call PRINT_SCORE    
    
    ; Display Score(String)
    mov SI, offset SCORE_MSG
    mov DH, 01h
    mov DL, 04h
    mov BL, 07h  
    call PUTS_STRING  
    
    ; Top Line of the Game
    ;-------------------------------   
    mov DX, 010Ch
    call GOTO_XY
    print_str GAME_FIELD_GORIZONT
    
    ; Side Lines Game
    mov CX,16h
    mov DX,010Ch        
    LOOP_SCREEN_GAME:
        inc DH
        call GOTO_XY
        print_str GAME_SIDE_VERTICAL
    loop LOOP_SCREEN_GAME
                        
    ; Bottom Line of the Game
    mov DX, 170Ch
    call GOTO_XY
    print_str GAME_FIELD_GORIZONT
    
    ; Print Next Figure Box
    ;-------------------------------    
    mov  SI, offset PIECE_BOX_BASE
    mov  DX, 011Bh
    call GOTO_XY
    print_str PIECE_BOX_BASE
   
    mov CX,06h
    LOOP_SCREEN_BOX:
    mov  SI, offset PART_BOX_SIDE
        inc DH  
        call GOTO_XY
        print_str PART_BOX_SIDE 
    loop LOOP_SCREEN_BOX
    
    mov DH, 08h
    call GOTO_XY
    print_str PIECE_BOX_BASE
    
    ; Print First Piece
    ;-------------------------------
    call GAMEPLAY
    PopAXBXCXDX
    ret
endp
