PRINT_SCORE proc
    PushAXBXCXDX
    mov DH, 02h                     ; Adjust(regulirovat') Line 
    mov DL, 0Ah                     ; Adjust Column (4 + 6) -> The value will also be used as a divisor
    mov BL, 06h                     ; Set minimum limit of characters 
    mov CX, word ptr GAME_SCORE
    
    DIGITAL_SCORE_LOOP:
        mov  AX, CX
        push DX
        mov  DX, 0h
        mov  CX, 0Ah                ; Set the divisor value = 10
        div  CX                     ; Divide AX by CX
        mov  CX, AX                 ; Save result value in CX
        mov  AX, DX                 ; Take the result of division and store it in AX
        add  AL, '0'                ; Add '0' to print correct character
        pop  DX

        ; Print Value on Screen
        push CX                  
        mov CX,1                    ; Informs that there is only one character to be displayed
        sub DX,2       
        call GOTO_XY                ; Go to position
        call CHAR_DISPLAY           ; Prints
        add DX,2
        pop CX
        cmp  DL, BL
        je   END_PRINT_SCORE
        dec  DL
    jmp  DIGITAL_SCORE_LOOP
    
    END_PRINT_SCORE:          
    popAXBXCXDX       
    ret  
endp
