PRINT_FIGURE proc ; al = number of figure ; ah [0 = write; 1 = erase]
    PushAXBXCXDX
    cmp  AL, 00d ; Figure T
    jne  try_figure_S
        mov BL, 04h            
        mov SI, OFFSET FIGURE_T
        jmp print_figure_now
    try_figure_S:
    cmp  AL, 01d ; Figure S
    jne  try_figure_Z
        mov BL, 06h            
        mov SI, OFFSET FIGURE_S
        jmp print_figure_now
    try_figure_Z:    
    cmp  AL, 02d ; Figure Z
    jne  try_figure_I
        mov BL, 0Eh            
        mov SI, OFFSET FIGURE_Z
        jmp print_figure_now
    try_figure_I:    
    cmp  AL, 03d ; Figure I
    jne  try_figure_O
        mov BL, 02h            
        mov SI, OFFSET FIGURE_I
        jmp print_figure_now
    try_figure_O:    
    cmp  AL, 04d ; Figure O
    jne  try_figure_J
        mov BL, 03h            
        mov SI, OFFSET FIGURE_O
        jmp print_figure_now
    try_figure_J:    
    cmp  AL, 05d ; Figure J
    jne  try_figure_L
        mov BL, 09h            
        mov SI, OFFSET FIGURE_J
        jmp print_figure_now
    try_figure_L:
        mov BL, 01h            
        mov SI, OFFSET FIGURE_L
        jmp print_figure_now

    print_figure_now:
    cmp ah, 1
    jne skip_and_print
    mov bl,0
    skip_and_print:
    mov AX,0
    call PRINT_GENERIC_FIGURE

    PopAXBXCXDX
ret
endp
