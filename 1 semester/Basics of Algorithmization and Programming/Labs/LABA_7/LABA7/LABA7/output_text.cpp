#include "Headers.h"                              //подключение библиотеки

void output_text(char** str, int &n)              //функция вывода на экран введенного текста
{
	puts("\nВведенный текст:");                   //вывод текста
	for (int i = 0; i < n; i++)                   //цикл по i для строк текста
	{
		printf("\n");                             //переход на новую строку
		for (int j = 0; (*(*(str + i) + j)) != '\0'; j++)   //цикл для вывода строки посимвольно
		{
			printf("%c", (*(*(str + i) + j)));    //вывод очередного символа
		}
	}
	printf("\n");                                 //переход на новую строку                     
}