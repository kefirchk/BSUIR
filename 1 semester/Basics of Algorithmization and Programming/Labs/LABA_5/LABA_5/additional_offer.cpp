#include "all_functions.h"                       //подключение библиотеки

void additional_offer(char choice)               //функция выбора, которая решает продолжить ли выполнение программы  
{
	char symbol;                                 //объявление промежуточной переменной
	printf("\n\nХотите продолжить?(y\\n)");        //приглашение на продолжение выполнения программы
	for (;;) {
		rewind(stdin);                           //чистка буфера
		scanf_s("%c", &choice);                  //ввод y или n(выбор для продолжения или нет)
		scanf_s("%c", &symbol);                  //ввод переменной symbol для совершения проверки 
		if (symbol == '\n' && (choice == 'y' || choice == 'n'))    //условие выхода из цикла
			{
				system("cls");                   //чистка консоли
				break;                           //выход из цикла
			}
		else {                                   //иначе
			printf("Ошибка!\n");                 //текст об ошибке
			printf("Хотите продолжить?(y\\n)");  
		}
	}
	if (choice == 'n') exit(0);                  //выход из цикла, если выполняется условие
}